package com.example.electrodomesticos;

public class TelevisonModel {

   String id, brand, inches, color, model, weight, measure, price, os, voltage, connectortype, screentype, maker, resolution, audio, accessory, serie;

   public TelevisonModel() {
   }

   public String getId() {
      return id;
   }

   public void setId(String id) {
      this.id = id;
   }

   public String getBrand() {
      return brand;
   }

   public void setBrand(String brand) {
      this.brand = brand;
   }

   public String getInches() {
      return inches;
   }

   public void setInches(String inches) {
      this.inches = inches;
   }

   public String getColor() {
      return color;
   }

   public void setColor(String color) {
      this.color = color;
   }

   public String getModel() {
      return model;
   }

   public void setModel(String model) {
      this.model = model;
   }

   public String getWeight() {
      return weight;
   }

   public void setWeight(String weight) {
      this.weight = weight;
   }

   public String getMeasure() {
      return measure;
   }

   public void setMeasure(String measure) {
      this.measure = measure;
   }

   public String getPrice() {
      return price;
   }

   public void setPrice(String price) {
      this.price = price;
   }

   public String getOs() {
      return os;
   }

   public void setOs(String os) {
      this.os = os;
   }

   public String getVoltage() {
      return voltage;
   }

   public void setVoltage(String voltage) {
      this.voltage = voltage;
   }

   public String getConnectortype() {
      return connectortype;
   }

   public void setConnectortype(String connectortype) {
      this.connectortype = connectortype;
   }

   public String getScreentype() {
      return screentype;
   }

   public void setScreentype(String screentype) {
      this.screentype = screentype;
   }

   public String getMaker() {
      return maker;
   }

   public void setMaker(String maker) {
      this.maker = maker;
   }

   public String getResolution() {
      return resolution;
   }

   public void setResolution(String resolution) {
      this.resolution = resolution;
   }

   public String getAudio() {
      return audio;
   }

   public void setAudio(String audio) {
      this.audio = audio;
   }

   public String getAccessory() {
      return accessory;
   }

   public void setAccessory(String accessory) {
      this.accessory = accessory;
   }

   public String getSerie() {
      return serie;
   }

   public void setSerie(String serie) {
      this.serie = serie;
   }
}


